// 1. git push adalah perintah yang digunakan untuk mengirimkan perubahan / upload file ke branch di remote repository.
"git push origin main";

// 2. git pull adalah perintah yang digunakan untuk mengambil perubahan yang ada di remote repository ke direktori lokal.
"git pull";

// 3. git init adalah perintah yang digunakan untuk membuat repository baru.
"git init";

// 4. git diff adalah perintah yang digunakan untuk menampilkan semua conflict yang ada.
"git merge";

// 5. git branch adalah perintah yang digunakan untuk melihat ada branch apa saja yang ada di repository.
"git branch";